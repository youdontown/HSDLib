    /// <summary>
    /// 
    /// </summary>
    public class SBM_DynamicBehaviorIDs : HSDAccessor
    {
        public short[] Values
        {
            get
            {
                var e = new short[_s.Length / 2];

                for (int i = 0; i < e.Length; i++)
                    e[i] = _s.GetInt16(i * 2);

                return e;
            }
            set
            {
                if (value == null)
                {
                    _s.Resize(4);
                    _s.SetInt32(0, 0);
                }
                else
                {
                    var size = value.Length * 2;
                    if (size % 4 != 0)
                        size += 4 - (size % 4);

                    _s.Resize(size);
                    for (int i = 0; i < value.Length; i++)
                        _s.SetInt16(i * 2, value[i]);
                }
            }
        }
    }

